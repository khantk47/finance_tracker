    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm=
             (javax.swing.table.DefaultTableModel)uptable.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            java.time.LocalDate cd=java.time.LocalDate.now();
            java.time.LocalDate bd=cd.minusDays(20);
            ResultSet rs=db.Dbconnect.st.executeQuery("select * from expense_info where e_date<='"+cd+"' and e_date>='"+bd+"' ");
            int eid=0;
            int total=0;
            while(rs.next()){
               total+=rs.getInt("Amount");
                 eid=rs.getInt("eid");
                String Date=rs.getString("e_date");
                String Amount=rs.getString("Amount");
                String Category=rs.getString("category");
                
            {
                Object o[]={++ eid,Date,Category,Amount};
                dtm.addRow(o);
            }
        }
         totalAmount.setText(total+"");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
     private void displayCategory(){
        try{
            category.removeAllItems();
           ResultSet rs= db.Dbconnect.st.executeQuery("select* from category_info");
           while(rs.next()){
               category.addItem(rs.getString("category"));
           }
                    
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
