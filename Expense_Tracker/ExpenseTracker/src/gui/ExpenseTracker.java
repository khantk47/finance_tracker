    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            java.util.Date dt=D.getDate();
            
            String s1=A.getText();
            String c=(String)category.getSelectedItem();
            if(dt!=null && !s1.equals("") && !c.equals("")){
                int amount=Integer.parseInt(s1);
                java.sql.Date date=new java.sql.Date(dt.getTime());
                db.Dbconnect.st.executeUpdate("insert into expense_info( e_date,category,amount) values('"+date+"','"+c+"','"+amount+"')");
                JOptionPane.showMessageDialog(null , "Data Added sucessfully!!!");
                getEntries();
            }else{
                JOptionPane.showMessageDialog(null , "Please enter input");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

